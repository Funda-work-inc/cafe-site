document.addEventListener('DOMContentLoaded', function() {
    
    // üé≠ „Éë„É©„É©„ÉÉ„ÇØ„ÇπÂäπÊûú
    function initParallax() {
        window.addEventListener('scroll', () => {
            const scrolled = window.pageYOffset;
            const parallaxElements = document.querySelectorAll('.parallax');
            
            parallaxElements.forEach(element => {
                const speed = element.dataset.speed || 0.5;
                const yPos = -(scrolled * speed);
                element.style.transform = `translate3d(0, ${yPos}px, 0)`;
            });
        });
    }

    // ‚ú® „Çπ„ÇØ„É≠„Éº„É´ÊôÇ„Éï„Çß„Éº„Éâ„Ç§„É≥
    function initScrollAnimations() {
        const observerOptions = {
            threshold: 0.1,
            rootMargin: '0px 0px -50px 0px'
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('animate-in');
                }
            });
        }, observerOptions);

        // „Ç¢„Éã„É°„Éº„Ç∑„Éß„É≥ÂØæË±°Ë¶ÅÁ¥†„ÇíÁõ£Ë¶ñ
        document.querySelectorAll('.fade-in, .slide-up, .scale-in').forEach(el => {
            observer.observe(el);
        });
    }

    // üåä „Éû„Ç¶„Çπ„Éï„Ç©„É≠„ÉºÂäπÊûú
    function initMouseFollow() {
        const cursor = document.createElement('div');
        cursor.className = 'custom-cursor';
        document.body.appendChild(cursor);

        document.addEventListener('mousemove', (e) => {
            cursor.style.left = e.clientX + 'px';
            cursor.style.top = e.clientY + 'px';
        });

        // „Éõ„Éê„ÉºÊôÇ„ÅÆ„Ç´„Éº„ÇΩ„É´Â§âÂåñ
        document.querySelectorAll('a, button, .clickable').forEach(el => {
            el.addEventListener('mouseenter', () => cursor.classList.add('hover'));
            el.addEventListener('mouseleave', () => cursor.classList.remove('hover'));
        });
    }

    // üé® ÊñáÂ≠ó„Çø„Ç§„Éî„É≥„Ç∞ÂäπÊûú
    function initTypingEffect() {
        const typingElements = document.querySelectorAll('.typing-effect');
        
        typingElements.forEach(element => {
            const text = element.textContent;
            element.textContent = '';
            element.style.opacity = '1';
            
            let i = 0;
            const timer = setInterval(() => {
                if (i < text.length) {
                    element.textContent += text.charAt(i);
                    i++;
                } else {
                    clearInterval(timer);
                }
            }, 100);
        });
    }

    // üåü „Ç≠„É©„Ç≠„É©ÂäπÊûú
    function createSparkles() {
        const sparkleContainer = document.createElement('div');
        sparkleContainer.className = 'sparkle-container';
        document.body.appendChild(sparkleContainer);

        function createSparkle() {
            const sparkle = document.createElement('div');
            sparkle.className = 'sparkle';
            sparkle.style.left = Math.random() * window.innerWidth + 'px';
            sparkle.style.top = Math.random() * window.innerHeight + 'px';
            sparkle.style.animationDelay = Math.random() * 3 + 's';
            
            sparkleContainer.appendChild(sparkle);
            
            setTimeout(() => {
                sparkle.remove();
            }, 3000);
        }

        setInterval(createSparkle, 500);
    }

    // üéµ „Çπ„É†„Éº„Çπ„Çπ„ÇØ„É≠„Éº„É´
    function initSmoothScroll() {
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    target.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
            });
        });
    }

    // üåà „Ç´„É©„Éº„ÉÅ„Çß„É≥„Ç∏ÂäπÊûú
    function initColorWave() {
        let hue = 0;
        setInterval(() => {
            hue = (hue + 1) % 360;
            document.documentElement.style.setProperty('--accent-wave', `hsl(${hue}, 70%, 60%)`);
        }, 100);
    }

    // üì± „É¨„Çπ„Éù„É≥„Ç∑„Éñ„É°„Éã„É•„Éº
    function initMobileMenu() {
        const header = document.querySelector('header');
        let lastScrollTop = 0;
        
        window.addEventListener('scroll', () => {
            const scrollTop = window.pageYOffset;
            
            if (scrollTop > lastScrollTop && scrollTop > 100) {
                header.classList.add('header-hidden');
            } else {
                header.classList.remove('header-hidden');
            }
            
            lastScrollTop = scrollTop;
        });
    }

    // üé™ „ÇÆ„É£„É©„É™„Éº„Éõ„Éê„ÉºÂäπÊûú
    function initGalleryEffects() {
        document.querySelectorAll('.gallery-item').forEach(item => {
            item.addEventListener('mouseenter', function() {
                this.style.transform = 'scale(1.05) rotate(2deg)';
                this.style.boxShadow = '0 20px 40px rgba(0,0,0,0.3)';
            });
            
            item.addEventListener('mouseleave', function() {
                this.style.transform = 'scale(1) rotate(0deg)';
                this.style.boxShadow = 'none';
            });
        });
    }

    // üå∏ „Éï„É≠„Éº„ÉÜ„Ç£„É≥„Ç∞Ë¶ÅÁ¥†
    function initFloatingElements() {
        const floatingElements = document.querySelectorAll('.float');
        
        floatingElements.forEach((el, index) => {
            el.style.animationDelay = `${index * 0.5}s`;
        });
    }

    // üéä ÂàùÊúüÂåñÂÆüË°å
    initParallax();
    initScrollAnimations();
    initMouseFollow();
    initSmoothScroll();
    initMobileMenu();
    initGalleryEffects();
    initFloatingElements();
    createSparkles();
    
    // „Éö„Éº„Ç∏„É≠„Éº„ÉâÂÆå‰∫ÜÂæå„ÅÆÁâπÂà•ÂäπÊûú
    setTimeout(() => {
        initTypingEffect();
        initColorWave();
    }, 1000);

    // üì± „Éè„É≥„Éê„Éº„Ç¨„Éº„É°„Éã„É•„ÉºÂàùÊúüÂåñ
    initHamburgerMenu();
    
    console.log('‚ú® Le Ciel √©toil√© Caf√© - ÂãïÁöÑ„É¢„Éº„Ç∑„Éß„É≥ÂàùÊúüÂåñÂÆå‰∫ÜÔºÅ ‚ú®');
});

// üì± „Éè„É≥„Éê„Éº„Ç¨„Éº„É°„Éã„É•„ÉºÊ©üËÉΩ
function initHamburgerMenu() {
    const hamburger = document.getElementById('hamburger');
    const nav = document.getElementById('nav');
    const navLinks = document.querySelectorAll('.nav a');
    
    if (!hamburger || !nav) return;
    
    // „Éè„É≥„Éê„Éº„Ç¨„Éº„Éú„Çø„É≥„ÇØ„É™„ÉÉ„ÇØ
    hamburger.addEventListener('click', function(e) {
        e.stopPropagation();
        toggleMenu();
    });
    
    // „Éä„Éì„É™„É≥„ÇØ„ÇØ„É™„ÉÉ„ÇØÊôÇ„Å´„É°„Éã„É•„Éº„ÇíÈñâ„Åò„Çã
    navLinks.forEach(link => {
        link.addEventListener('click', function() {
            closeMenu();
        });
    });
    
    // ËÉåÊôØ„ÇØ„É™„ÉÉ„ÇØÊôÇ„Å´„É°„Éã„É•„Éº„ÇíÈñâ„Åò„Çã
    document.addEventListener('click', function(e) {
        if (nav.classList.contains('active') && !nav.contains(e.target)) {
            closeMenu();
        }
    });
    
    // ESC„Ç≠„ÉºÊôÇ„Å´„É°„Éã„É•„Éº„ÇíÈñâ„Åò„Çã
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape' && nav.classList.contains('active')) {
            closeMenu();
        }
    });
    
    // „Ç¶„Ç£„É≥„Éâ„Ç¶„É™„Çµ„Ç§„Ç∫ÊôÇ„ÅÆÂá¶ÁêÜ
    window.addEventListener('resize', function() {
        if (window.innerWidth > 768 && nav.classList.contains('active')) {
            closeMenu();
        }
    });
    
    function toggleMenu() {
        hamburger.classList.toggle('active');
        nav.classList.toggle('active');
        
        // „Éú„Éá„Ç£„ÅÆ„Çπ„ÇØ„É≠„Éº„É´Âà∂Âæ°
        if (nav.classList.contains('active')) {
            document.body.style.overflow = 'hidden';
        } else {
            document.body.style.overflow = 'auto';
        }
    }
    
    function closeMenu() {
        hamburger.classList.remove('active');
        nav.classList.remove('active');
        document.body.style.overflow = 'auto';
    }
}

// üñºÔ∏è „É©„Ç§„Éà„Éú„ÉÉ„ÇØ„ÇπÊ©üËÉΩ
function openLightbox(imageSrc) {
    event.stopPropagation();
    
    const lightbox = document.getElementById('lightbox');
    const lightboxImg = document.getElementById('lightbox-img');
    
    lightboxImg.src = imageSrc;
    lightbox.style.display = 'flex';
    
    setTimeout(() => {
        lightbox.classList.add('show');
    }, 10);
    
    document.body.style.overflow = 'hidden';
}

function closeLightbox() {
    const lightbox = document.getElementById('lightbox');
    
    lightbox.classList.remove('show');
    
    setTimeout(() => {
        lightbox.style.display = 'none';
    }, 300);
    
    document.body.style.overflow = 'auto';
}

